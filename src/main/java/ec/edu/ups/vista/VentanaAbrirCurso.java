    private void btnAbrirCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirCursoActionPerformed
        boolean resultado = false;
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nombre = txtNombre.getText();
        Curso c = new Curso(codigo);
        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        } else {
            if (controladorCurso.read(c) != null) {
                Curso curso = new Curso(codigo, nombre);
                resultado = controladorCurso.update(curso);
            } else {
                Curso curso = new Curso(codigo, nombre);
                resultado = controladorCurso.create(curso);
            }
            limpiar();
            JOptionPane.showMessageDialog(this, "Operaci√≥n : " + resultado);
            dispose();
        }
       
    }//GEN-LAST:event_btnAbrirCursoActionPerformed

    private void tblCursoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCursoMouseClicked
        int fila = tblCurso.getSelectedRow();
        int cod = (int) tblCurso.getValueAt(fila, 0);
        String nombre = (String) tblCurso.getValueAt(fila, 1);
        
        txtCodigo.setText(String.valueOf(cod));
        txtNombre.setText(nombre);
    }//GEN-LAST:event_tblCursoMouseClicked


