    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       controladorActividades.ingresarRegex("(http(s)?\\:\\/\\/)(www)?(.[^\"\\s])+");
       StringBuilder stringBuilder=new StringBuilder();
       String textoBusqueda=txtActividad.getText();
       
       try{
           URL urlObject = new URL("https://play.google.com/store/search?q=" + textoBusqueda.replaceAll("\\s", "\\+"));
           URLConnection urlConnection = urlObject.openConnection();
           urlConnection.setRequestProperty("User-Agent", "Mozilla/5.0 (Windows NT 6.1; WOW64) AppleWebKit/537.11 (KHTML, like Gecko) Chrome/23.0.1271.95 Safari/537.11");

           BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(urlConnection.getInputStream(), "UTF-8"));
           String inputLine;
           while ((inputLine = bufferedReader.readLine()) != null) {
               stringBuilder.append(inputLine);
           }
        }catch(IOException e){
            
        }
       
        resultado= controladorActividades.obtenerUrlGoogle(stringBuilder.toString());
        Lista(resultado);
       //resultado.stream().forEach(s-> System.out.println(s.replaceAll("<a\\shref=\"","")));
       
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean resultad = false;
        String nombre= txtActividad.getText();
        
        Actividades act= new Actividades(nombre);
        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        }else{
            if (controladorActividades.read(act) != null) {
                Actividades actividades= new Actividades(nombre, resultado);
                resultad = controladorActividades.update(actividades);
            } else {
                Actividades actividades= new Actividades(nombre, resultado);
                resultad = controladorActividades.create(actividades);
            }
            limpiar();
            JOptionPane.showMessageDialog(this, "OperaciÃ³n : " + resultad);
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

