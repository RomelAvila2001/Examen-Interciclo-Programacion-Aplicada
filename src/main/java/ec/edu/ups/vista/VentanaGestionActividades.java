    public void Lista (Set<String> aplicaciones) {
        DefaultListModel modelo = new DefaultListModel();
        modelo.clear();
        for (String nombre : aplicaciones) {
            modelo.addElement(nombre);
        }
        Lista.setModel(modelo);
    }
    
     public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblActividades.getModel();

        modeloTabla.setRowCount(0);
        for (Actividades actividades : controladorActividades.getListaGenerica()) {
            Object[] rowData = {actividades.getNombre()};
            modeloTabla.addRow(rowData);
        }
        tblActividades.setModel(modeloTabla);
    }

