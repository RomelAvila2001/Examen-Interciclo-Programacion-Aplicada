    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre= Lista.getSelectedValue();
        Alumno alum= new Alumno(nombre);
        Alumno alumno= controladorAlumno.read(alum);
        
        txtCodigo.setText(String.valueOf(alumno.getCodigo()));
        txtCedula.setText(alumno.getCedula());
        txtNombre.setText(alumno.getNombreApellido());
       
        txtComportamiento.setText(alumno.getComportamiento());
        
        Curso cur= alumno.getCurso();
        txtCodigoCurso.setText(String.valueOf(cur.getCodigo()));
        txtNombreCurso.setText(cur.getNombre());
        
        txtDatosInformacion.setText(alumno.toString());

    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int eliminar = JOptionPane.showConfirmDialog(this, "¿Seguro quieres eliminar este Alumno?");
        Alumno alum= new Alumno(txtNombre.getText());
        Alumno alumno= controladorAlumno.read(alum);
        if(eliminar == JOptionPane.YES_OPTION){
            boolean resultado = controladorAlumno.delete(alumno);
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            limpiar();
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


