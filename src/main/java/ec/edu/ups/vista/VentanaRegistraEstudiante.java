    private void btnRegistrarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAlumnoActionPerformed
        boolean resultado = false;
        int codigo=Integer.parseInt(txtCodigo.getText());
        String cedula= txtCedula.getText();
        String nombre= txtNombre.getText();
        String comportamineto=txtComportamiento.getText();
        String codigoCurso= txtCodigoCurso.getText();
        String nombreCurso= txtNombreCurso.getText();
        
        int codigoc=Integer.parseInt(txtCodigoCurso.getText());
        Curso c = new Curso(codigoc);
        Curso curso=controladorCurso.read(c);
        
        Alumno alum= new Alumno(nombre);

        if(cedula.isEmpty()||nombre.isEmpty()||comportamineto.isEmpty()||codigoCurso.isEmpty()||nombreCurso.isEmpty()){
            JOptionPane.showMessageDialog(this, "Llene todos los campos solicitados");
        }else{
            if(controladorAlumno.read(alum)!= null){
                Alumno alumno = new Alumno(codigo, comportamineto, curso, cedula, nombre);
                resultado =controladorAlumno.update(alumno);
                
            }else{
                Alumno alumno = new Alumno(codigo, comportamineto, curso, cedula, nombre);
                resultado =controladorAlumno.create(alumno);
                
            }
            
            limpiar();
            JOptionPane.showMessageDialog(this, "Operación : " + resultado);
            cargarDatos();
            dispose();
        }
    }//GEN-LAST:event_btnRegistrarAlumnoActionPerformed

