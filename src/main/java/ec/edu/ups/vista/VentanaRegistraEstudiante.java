    public List<Alumno> listaAlumno(){
        List<Alumno> listaAlumnos;
        listaAlumnos = new ArrayList<>();
        for (Alumno alumno : controladorAlumno.getListaGenerica()){
            Curso curso= docente.getCurso();
            Curso curso2= alumno.getCurso();
            System.out.println(curso);
            System.out.println(curso2);
            if(curso.equals(curso2)){
                listaAlumnos.add(alumno);
            }
        }
        return listaAlumnos;
    }
    
    
    public void cargarCodigo() {
        int cod = controladorAlumno.cargarCodigo();
        txtCodigo.setText(String.valueOf(cod));
    }
    
    public void cargarDatos() {
       DefaultTableModel modeloTabla = (DefaultTableModel) tblCurso.getModel();

        modeloTabla.setRowCount(0);
        for (var curso : controladorCurso.getListaGenerica()) {
            Object[] rowData = {curso.getCodigo(), curso.getNombre()};
            modeloTabla.addRow(rowData);
        }
        tblCurso.setModel(modeloTabla);
    }
    public void ListaEstu () {
        DefaultListModel modelo = new DefaultListModel();
        modelo.clear();
        
        for (Alumno alumno : controladorAlumno.getListaGenerica()) {
            modelo.addElement(alumno.getNombreApellido());
        }
        Lista.setModel(modelo);
    }
    
